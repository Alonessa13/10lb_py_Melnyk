def fibonacci_recursive(n):
    """
    Обчислює n-те число Фібоначчі рекурсивно.
    
    Аргументи:
    n -- ціле невід'ємне число
    
    Повертає:
    n-те число Фібоначчі
    """
    if not isinstance(n, int):
        raise TypeError("Потрібно ввести ціле число.")
    if n < 0:
        raise ValueError("Число Фібоначчі не визначено для від'ємних чисел.")
    if n == 0:
        return 0
    if n == 1:
        return 1
    return fibonacci_recursive(n - 1) + fibonacci_recursive(n - 2)

# Пояснення: рекурсивний спосіб тут менш ефективний для великих n,
# бо багато разів обчислюються однакові значення. Ітеративний підхід працює швидше.
